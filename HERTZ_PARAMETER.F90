	
	!This subroutine is to determine the Hertz contact parameters
	SUBROUTINE HERTZ_PARAMETER(WLD,R11,R12,R21,R22,EM1,EM2,E1,E2,A0,B0,RX,RY,R0,P0,AKE,E0,E00,EV,EVV)
	IMPLICIT NONE
	REAL*8 WLD,R11,R12,R21,R22,EM1,EM2,E1,E2,A0,B0,P0,AKE
	REAL*8 AA1,AA2,AA3,AA4,E0,E00,EV,EVV,RX,RY,ZQ,ZYX,R0,RI2,PI
	
    PI  = 4.0*ATAN(1.0D0) 
	AA1= (1.-EM1*EM1)/E1+(1.-EM2*EM2)/E2
	AA2= (1.+EM1)/E1+(1.+EM2)/E2
    E0 = 1./AA1
    E00= 1./AA2
	AA3= EM1*(1.+EM1)/E1+EM2*(1.+EM2)/E2	
	AA4= (1.+EM1)*(1.-2.*EM1)/2./E1-(1.+EM2)*(1.-2.*EM2)/2.0/E2	
 	EV = 1./AA3
	EVV= 1./AA4

    RX = 1.0/R11+1.0/R21
    RX = 1.0/RX
    RY = 1.0/R12+1.0/R22
    RY = 1.0/RY
    ZQ = 1.0/RX+1.0/RY
    ZQ = 1.0/ZQ
    ZYX= RY/RX
	R0 = SQRT(RX*RY)
    AKE= 1.0339*ZYX**0.6360
    IF(ABS(RX-RY).LT.0.00001) AKE=1.0

	IF(R11==R12.AND.R21==R22) THEN
    B0=(3.0*WLD*ZQ/(2.0*E0))**0.3333333333333333
	A0=B0
	ELSE
    RI2= 1.0003+0.5968/ZYX
    B0 = 6.0*AKE*AKE*RI2*ZQ*WLD/(2.0*PI*E0)
    B0 = B0**0.333333333333
    A0 = B0/AKE
    ENDIF
    P0 = 1.5*WLD/PI/B0/A0
	
	END
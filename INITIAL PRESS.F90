
	SUBROUTINE PINITIAL(M,N,P,XIN,YIN,DX,DY)	!给定压力初值
    IMPLICIT NONE
	INTEGER M,N,I,J
	REAL*8 XIN,YIN,XY,DX,DY,X,Y
	REAL*8 P(M,N)

    DO I=1,M
		X=XIN+DX*(I-1)
		DO J=1,N
			Y=YIN+DY*(J-1)
			XY=X**2+Y**2
			IF(XY <= 1) THEN
				P(I,J)=SQRT(1-XY)
			ELSE
				P(I,J)=0.0
			END IF
		ENDDO
	ENDDO

    END
